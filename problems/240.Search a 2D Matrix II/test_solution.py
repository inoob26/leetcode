from solution import Solution


def test_solution_true():
    matrix = [
        [1, 4, 7, 11, 15],
        [2, 5, 8, 12, 19],
        [3, 6, 9, 16, 22],
        [10, 13, 14, 17, 24],
        [18, 21, 23, 26, 30],
    ]
    target = 5
    expected = True
    result = Solution().searchMatrix(matrix, target)
    assert expected == result


def test_solution_false():
    matrix = [
        [1, 4, 7, 11, 15],
        [2, 5, 8, 12, 19],
        [3, 6, 9, 16, 22],
        [10, 13, 14, 17, 24],
        [18, 21, 23, 26, 30],
    ]
    target = 20
    expected = False
    result = Solution().searchMatrix(matrix, target)
    assert expected == result
